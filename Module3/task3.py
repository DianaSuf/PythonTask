# Детали
shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]
detail = input("Название детали: ")
counter = 0
sum = 0

for i in shop:
    if detail == i[0]:
        counter += 1
        sum += i[1]

print("Количество деталей: ", counter)
print("Общая стоимость: ", sum)